#include <SIMPLETL/PDU.hpp>
#include <SIMPLETL/PCI.hpp>

simpletl::PDU::PDU(simpletl::PCI* pci, const wns::osi::PDUPtr& pdu)
	:
	wns::osi::PDU(pci, pdu.getPtr())
{}

Bit simpletl::PDU::doGetLengthInBits() const
{
	return getPCI()->getSize() + getUserData()->getLengthInBits();
}
